def message_loop():
    pixels = len(sequence)*7
    scroll = 0
    matrix.clear_all_bytes()
    while scroll != - pixels:
        for i in range(0, len(sequence)):
            if scroll+i*7 < COL_SIZE or scroll+i*7 > 0:
                printat(8, scroll+i*7, sequence[i], 255)
        scroll -= 1

